/**
 * Enum representing the different roles in the system.
 */
export enum ROLE_TYPE {
  /**
   * Role for Super Admin with the highest level of access and privileges.
   */
  SUPER_ADMIN = 'SUPER_ADMIN',

  /**
   * Role for Admin users with administrative privileges.
   */
  ADMIN = 'ADMIN',

  /**
   * Role for regular users with standard access and privileges.
   */
  USER = 'USER'
}
